    def get_recent_memories(self, count: int = 10) -> List[Observation]:
        """Get the most recent memories"""
        return sorted(self.observations, key=lambda x: x.timestamp, reverse=True)[:count]

    def get_memories_about(self, topic: str, count: int = 5) -> List[Observation]:
        """Get memories related to a topic"""
        return self.retrieve(query=topic, k=count)

    def update_scratch(self, **kwargs):
        """Update short-term working memory"""
        for key, value in kwargs.items():
            if hasattr(self.scratch, key):
                setattr(self.scratch, key, value)

    def save(self, filepath: str):
        """Save memory to JSON file"""
        data = {
            "agent_id": self.agent_id,
            "observations": [obs.to_dict() for obs in self.observations],
            "reflections": [refl.to_dict() for refl in self.reflections],
            "scratch": {
                "current_task": self.scratch.current_task,
                "current_location": self.scratch.current_location,
                "recent_context": self.scratch.recent_context,
                "working_notes": self.scratch.working_notes,
            },
        }
        with open(filepath, 'w') as f:
            json.dump(data, f, indent=2)

    @classmethod
    def load(cls, filepath: str) -> "AgentMemory":
        """Load memory from JSON file"""
        with open(filepath, 'r') as f:
            data = json.load(f)

        memory = cls(agent_id=data["agent_id"])
        memory.observations = [Observation.from_dict(obs) for obs in data["observations"]]
        memory.reflections = [
            Reflection(
                id=refl["id"],
                agent_id=refl["agent_id"],
                insight=refl["insight"],
                source_observations=refl["source_observations"],
                timestamp=datetime.fromisoformat(refl["timestamp"]),
                importance=refl["importance"],
            )
            for refl in data["reflections"]
        ]
        memory.scratch.current_task = data["scratch"].get("current_task")
        memory.scratch.current_location = data["scratch"].get("current_location", "tavern")
        memory.scratch.recent_context = data["scratch"].get("recent_context", [])
        memory.scratch.working_notes = data["scratch"].get("working_notes", {})

        return memory

    def get_context_summary(self, max_tokens: int = 500) -> str:
        """
        Generate a summary of agent's memory for inclusion in prompt.

        Returns a concise summary of:
        - Current task
        - Recent important memories
        - Relevant reflections
        """
        summary_parts = []

        if self.scratch.current_task:
            summary_parts.append(f"Current task: {self.scratch.current_task}")

        recent_memories = self.get_recent_memories(count=5)
        if recent_memories:
            summary_parts.append("\nRecent experiences:")
            for mem in recent_memories:
                summary_parts.append(f"- {mem.content}")

        if self.reflections:
            summary_parts.append("\nKey insights:")
            for refl in self.reflections[-3:]:  # Last 3 reflections
                summary_parts.append(f"- {refl.insight}")

        return "\n".join(summary_parts)


